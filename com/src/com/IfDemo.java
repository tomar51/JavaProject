package com;

public class IfDemo {
	public static void main (String[]args) {
		int a = 10;
		if (a%2==0)
		{
			System.out.println("divided by 2");
		}
		else
		{
			System.out.println("not divided by 2");
		}
		if (a%4==0)
		{
			System.out.println("divided by 4");
		}
		else
		{
			System.out.println(" not divided by 4");
		}
	}

}
