package switchEx;

public class Demo {
	
	public static void main(String[]args) {
		
		int choice =1;
		
		switch(choice){
			
			case 1:
				
				System.out.println("bjp");
				
				break;
			
				case 2:
					
					System.out.println("app");	
					
					break;
					
				case 3:
					
					System.out.println("other");
					
					break;
					
					
			  default:
			  {
				   
				  System.out.println("no choice");
			  }
			  
		}	
			
			
		}		
		
	

}
